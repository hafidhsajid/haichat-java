    private void runThread() {
        while (true) {
            try {
                byte buf[] = new byte[1024];
                DatagramPacket incomingPacket = new DatagramPacket(buf, buf.length);
                socket.receive(incomingPacket);
                byte[] data = incomingPacket.getData();
                ByteArrayInputStream in = new ByteArrayInputStream(data);
                ObjectInputStream is = new ObjectInputStream(in);
                
                Chat chat = (Chat) is.readObject();
                System.out.println(chat.getPenerima().getUsername());
                if (chat.getPenerima().getUsername().equals(Session.session.getUsername())
                        && chat.getPengirim().getUsername().equals(penerima.getUsername())
                        ) {
                    String text = chatArea.getText();
                    text += chat.getPengirim().getNama() + " : " + chat.getIsichat() + "\n";
                    chatArea.setText(text);
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
    
    private void siapkanIP() {
        try {
            address = InetAddress.getByName("234.5.6.7");
            socket = new MulticastSocket(1234);
            socket.joinGroup(address);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

