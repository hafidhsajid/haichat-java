    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!inputChat.getText().equals("")) {
            try {
                Chat chat = new Chat();
                chat.setIsichat(inputChat.getText());
                chat.setPengirim(Session.session);
                chat.setPenerima(penerima);
                
                ByteArrayOutputStream outputStream = new ByteArrayOutputStream();
                ObjectOutputStream os = new ObjectOutputStream(outputStream);
                os.writeObject(chat);
                
                byte[] data = outputStream.toByteArray();
                
                dpSender = new DatagramPacket(data, 0, data.length, address, 1234);
                socket.send(dpSender);
                
                String text = chatArea.getText();
                text += chat.getPengirim().getNama() + " : " + chat.getIsichat() + "\n";
                chatArea.setText(text);
                inputChat.setText("");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

