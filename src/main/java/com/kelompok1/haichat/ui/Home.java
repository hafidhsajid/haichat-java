    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (listTeman.getSelectedIndex() == -1)
            JOptionPane.showMessageDialog(null, "Pilih orangnya dulu");
        else {
            if (listKontak.get(listTeman.getSelectedIndex()).getUser1().getNama().equals(Session.session.getNama())) {
                new ChatUI(listKontak.get(listTeman.getSelectedIndex()).getUser2()).setVisible(true);
            } else {
                new ChatUI(listKontak.get(listTeman.getSelectedIndex()).getUser1()).setVisible(true);
            }
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

