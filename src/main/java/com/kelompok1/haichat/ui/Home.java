    private void getKontak() {
        DefaultListModel listModel = new DefaultListModel();
        listKontak = new Kontak().getAllKontak(Session.session.getId());
        for (Kontak kontak : listKontak) {
            if (kontak.getUser2().getNama().equals(Session.session.getNama())) {
                listModel.addElement(kontak.getUser1().getNama());
            } else {
                listModel.addElement(kontak.getUser2().getNama());
            }
        }
        listTeman.setModel(listModel);
    }

